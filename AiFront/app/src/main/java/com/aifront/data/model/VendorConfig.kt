    fun getAllVendors(): List<Vendor> = vendors

    fun getAllModels(): List<AIModel> = models

    fun getModelsByVendor(vendorId: String): List<AIModel> =
        models.filter { it.vendorId == vendorId }

    fun getVendorById(vendorId: String): Vendor? =
        vendors.find { it.id == vendorId }

    fun getModelById(modelId: String): AIModel? =
        models.find { it.id == modelId }

    fun getTextModels(): List<AIModel> =
        models.filter { it.modelType == ModelType.TEXT }

    fun getImageModels(): List<AIModel> =
        models.filter { it.modelType == ModelType.IMAGE }

    fun getCodeModels(): List<AIModel> =
        models.filter { it.supportsCode }

    fun getVendorsByCountry(country: String): List<Vendor> =
        vendors.filter { it.country == country }

    fun getDomesticVendors(): List<Vendor> =
        vendors.filter { it.country == "中国" }

    fun getForeignVendors(): List<Vendor> =
        vendors.filter { it.country != "中国" }

    fun getLatestModels(): List<AIModel> =
        models.filter { it.isLatest }

    fun getBestPerformanceModels(): List<AIModel> =
        models.filter { it.isBestPerformance }

    fun getMultimodalModels(): List<AIModel> =
        models.filter { it.modelType == ModelType.MULTIMODAL }

    fun getModelsByType(modelType: ModelType): List<AIModel> =
        models.filter { it.modelType == modelType }

    fun searchModels(query: String): List<AIModel> =
        models.filter {
            it.name.contains(query, ignoreCase = true) ||
            it.displayName.contains(query, ignoreCase = true) ||
            it.description.contains(query, ignoreCase = true)
        }

    fun getVendorModelsWithVendor(vendorId: String): Pair<Vendor?, List<AIModel>> =
        Pair(getVendorById(vendorId), getModelsByVendor(vendorId))
